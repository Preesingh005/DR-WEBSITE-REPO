                                        <p class="mb-0">Disc prolapse, commonly referred to as a herniated or slipped disc, occurs when the soft inner core of an intervertebral disc pushes through the outer layer, compressing nearby nerves. This condition can cause severe pain, numbness, and weakness, often radiating down the legs. One of the most common complications of disc prolapse is sciatica, a condition where the sciatic nerve—the longest nerve in the body—becomes irritated or compressed. Sciatica typically leads to shooting pain, tingling sensations, and difficulty in movement, especially in the lower back, hips, and legs.</p>
                                        <p class="mb-0">The symptoms of sciatica vary depending on the severity of the disc prolapse. Patients often experience sharp, radiating pain along the sciatic nerve, which can extend from the lower back to the feet. Other symptoms include muscle weakness, numbness, and a burning sensation in the affected leg. The condition is often caused by age-related disc degeneration, improper posture, heavy lifting, or sudden injuries. Prolonged sitting, obesity, and lack of exercise also contribute to the worsening of disc-related issues, making early diagnosis and treatment essential.</p>
                                        <p class="mb-0">The treatment for disc prolapse and sciatica depends on the severity of symptoms. Conservative treatments include pain management through medications, physiotherapy, and lifestyle modifications. Physical therapy exercises help strengthen the back and core muscles, improving posture and reducing nerve compression. In severe cases where pain persists despite non-surgical methods, minimally invasive spine surgery such as microdiscectomy or lumbar decompression may be recommended. These procedures help relieve nerve pressure, allowing patients to regain mobility and a pain-free lifestyle.</p>
                                        
                                       
