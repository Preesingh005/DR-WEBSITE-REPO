                                        <p class="mb-0">Paralysis is the loss of muscle function in part or all of the body, often caused by damage to the nervous system. It can be temporary or permanent, partial or complete, depending on the severity and location of the injury. Paralysis occurs when signals between the brain and muscles are disrupted due to stroke, spinal cord injury, brain injury, nerve disorders, or conditions like multiple sclerosis and Guillain-Barré syndrome. The impact of paralysis on a person’s life depends on the extent of movement loss and the ability to regain function through treatment and rehabilitation.</p>
                                        <p class="mb-0">Paralysis can be classified based on the affected body parts. Monoplegia affects one limb, hemiplegia affects one side of the body, paraplegia affects both legs, and quadriplegia affects both arms and legs. Symptoms include loss of movement, muscle stiffness or flaccidity, numbness, and difficulty controlling bladder or bowel functions. In some cases, people with paralysis may experience pain, muscle spasms, or respiratory issues if the paralysis affects breathing muscles. The condition may develop suddenly, as in stroke, or gradually, as in progressive neurological disorders.</p>
