                                        <p class="mb-0">A seizure is a sudden, uncontrolled electrical disturbance in the brain that can cause changes in behavior, movements, sensations, or consciousness. Epilepsy is a chronic neurological disorder characterized by recurrent seizures. While seizures can occur due to fever, head injury, infections, or metabolic imbalances, epilepsy is diagnosed when a person experiences unprovoked seizures repeatedly. The severity of epilepsy varies, with some individuals experiencing occasional mild seizures and others having frequent, disabling episodes. Early diagnosis and treatment are crucial to managing the condition effectively.</p>
                                        <p class="mb-0">Seizures are classified into focal (partial) and generalized seizures. Focal seizures originate in a specific part of the brain and may cause twitching, confusion, or unusual sensations without loss of consciousness. Generalized seizures affect both brain hemispheres and include types such as absence seizures (brief loss of awareness), tonic-clonic seizures (violent convulsions and unconsciousness), and myoclonic seizures (sudden muscle jerks). Symptoms vary but may include staring spells, muscle stiffness, jerking movements, loss of consciousness, and temporary confusion. Identifying seizure triggers, such as lack of sleep, stress, or flashing lights, can help with prevention.</p>
                                        <p class="mb-0">Dr. Kunal Kumar, an experienced neurosurgeon, diagnoses epilepsy through EEG (electroencephalogram), MRI, CT scans, and blood tests to identify abnormal brain activity and underlying causes. Treatment options depend on the severity and type of seizures. Medications (antiepileptic drugs) help control seizures in most cases. For drug-resistant epilepsy, surgical procedures, such as lobectomy (removing the seizure-causing brain area) or vagus nerve stimulation (VNS), may be considered. Emerging therapies like ketogenic diets and deep brain stimulation (DBS) are also showing promising results for seizure management.</p>
                                        
                                       
