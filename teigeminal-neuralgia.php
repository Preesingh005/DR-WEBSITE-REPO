                                        <p class="mb-0">Trigeminal neuralgia (TN) is a chronic pain condition that affects the trigeminal nerve, which is responsible for sensation in the face. It is characterized by sudden, severe, and electric shock-like pain that typically occurs in the lower face and jaw but can also affect the area around the nose and eyes. The pain is often triggered by simple activities such as chewing, speaking, brushing teeth, or even a light touch to the face. Trigeminal neuralgia can significantly impact daily life, making even routine tasks unbearable.</p>
                                        <p class="mb-0">The primary cause of trigeminal neuralgia is often pressure on the trigeminal nerve, usually due to a blood vessel compressing it at the brainstem. In some cases, it can result from multiple sclerosis, tumors, facial trauma, or nerve damage. The condition is more common in people over 50 and tends to worsen over time. Symptoms include intense, stabbing facial pain that lasts from a few seconds to a few minutes, usually affecting one side of the face. The pain can occur in sudden attacks and may progressively become more frequent and severe.</p>
                                        <p class="mb-0">Dr. Kunal Kumar, a specialist in neurosurgery, diagnoses trigeminal neuralgia through clinical examinations, MRI scans, and nerve function tests to rule out other neurological conditions. The first line of treatment is usually medications like anticonvulsants (carbamazepine, gabapentin) or muscle relaxants, which help reduce nerve pain. In cases where medications fail, surgical options such as microvascular decompression (MVD), gamma knife radiosurgery, or nerve block procedures can provide long-term relief by reducing nerve compression and preventing pain signals.</p>
                                        
                                       
