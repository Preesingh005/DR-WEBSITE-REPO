                                        <p class="mb-0">A brain hemorrhage, also known as a hemorrhagic stroke, occurs when a blood vessel in the brain bursts, leading to bleeding in or around the brain. This condition is life-threatening and requires immediate medical attention. Brain hemorrhages can be caused by high blood pressure (hypertension), head trauma, aneurysms, blood clotting disorders, or arteriovenous malformations (AVMs). The bleeding disrupts normal brain function, causing neurological damage that can lead to paralysis, cognitive impairment, or even death if not treated promptly.</p>
                                        <p class="mb-0">The symptoms of a brain hemorrhage or stroke appear suddenly and depend on the affected brain region. Common signs include severe headaches, sudden weakness or numbness (especially on one side of the body), difficulty speaking, vision problems, dizziness, loss of coordination, and unconsciousness. Risk factors include uncontrolled high blood pressure, smoking, excessive alcohol consumption, diabetes, obesity, and a history of strokes or aneurysms. Recognizing symptoms early and seeking emergency care can significantly improve survival and recovery chances.</p>
